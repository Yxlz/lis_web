package com.cdxt.lisweb.model.right;

import java.util.List;

import com.cdxt.lisweb.entity.user.Right;

/**
 * @ClassName: Root
 * @Description: 菜单根节点
 * @author hezheng
 * @date 2017-5-4 上午10:52:52
 * @最后修改人：hezheng
 * @最后修改时间：2017-5-4 上午10:52:52
 */
public class Root {
	private String parentId;
	private List<Right> children;
	public Root(){}
	public Root(String parentId) {
		super();
		this.parentId = parentId;
	}

	public String getParentId() {
		return parentId;
	}

	public void setParentId(String parentId) {
		this.parentId = parentId;
	}

	public List<Right> getChildren() {
		return children;
	}

	public void setChildren(List<Right> children) {
		this.children = children;
	}
}
