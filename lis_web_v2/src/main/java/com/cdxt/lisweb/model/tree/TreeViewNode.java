package com.cdxt.lisweb.model.tree;

import java.util.List;

/**
 * bootstrap treeview 树 数据模型
 * 
 * @author zhaozeyu
 * @date 2018年1月9日 16:21:14
 */
public class TreeViewNode {
	private String id;
	private String text;// 节点文本
	private String icon;// 图标
	private String selectedIcon;// 选中后图标
	private String color;// 前景色
	private String backColor;// 背景色
	private String href;// TAB页地址
	private String rightCode;//权限代码
	private String parentCode;//父权限代码
	private String pinyin;//拼音代码
	private String reporturl;//报表类型菜单的路径
	private List<TreeViewNode> nodes;// 子节点LIST

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getSelectedIcon() {
		return selectedIcon;
	}

	public void setSelectedIcon(String selectedIcon) {
		this.selectedIcon = selectedIcon;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getBackColor() {
		return backColor;
	}

	public void setBackColor(String backColor) {
		this.backColor = backColor;
	}

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}

	public List<TreeViewNode> getNodes() {
		return nodes;
	}

	public void setNodes(List<TreeViewNode> nodes) {
		this.nodes = nodes;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRightCode() {
		return rightCode;
	}

	public void setRightCode(String rightCode) {
		this.rightCode = rightCode;
	}

	public String getParentCode() {
		return parentCode;
	}

	public void setParentCode(String parentCode) {
		this.parentCode = parentCode;
	}

	public String getPinyin() {
		return pinyin;
	}

	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}

	public String getReporturl() {
		return reporturl;
	}

	public void setReporturl(String reporturl) {
		this.reporturl = reporturl;
	}
}
